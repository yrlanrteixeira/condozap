export function getRoleCeiling(role: UserRole): string[] {
  return [...(roleCeiling[role] ?? [])];
}

export function intersectPermissions(
  ceiling: string[],
  requested: string[]
): string[] {
  const r = new Set(requested);
  return ceiling.filter((c) => r.has(c));
}
